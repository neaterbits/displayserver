package com.neaterbits.displayserver.xwindows.model;

public final class XBuiltinColor {

    private final int r;
    private final int g;
    private final int b;

    public XBuiltinColor(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getR() {
        return r;
    }

    public int getG() {
        return g;
    }

    public int getB() {
        return b;
    }
}
